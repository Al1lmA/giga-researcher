async def get_content_from_url(soup):

        text = ""
        tags = ['p', 'a', 'h1', 'h2', 'h3', 'h4', 'h5']
        for element in soup.find_all(tags):  
            text += element.text + "\n"
        
        return text

async def search_google(query, stop):
    # links = []
    result = []
    for i in search(query, tld="co.in", stop=stop, pause=5 ): 
        try:
            response = requests.get(i, headers=headers, verify=False)
            if response.status_code == 200:
                if not i.endswith(".pdf"):
                    # links.append(i)
                    soup = BeautifulSoup(response.content, "html.parser")
                    
                    # result.append(re.sub('\s+', ' ', get_content_from_url(soup)))
                    result.append(await get_content_from_url(soup))
        except Exception as er:
            logger.error(er)
    return result #, links

    

